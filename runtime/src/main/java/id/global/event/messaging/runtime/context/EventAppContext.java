package id.global.event.messaging.runtime.context;

public final class EventAppContext {
    private String id;
    private String title;
    private String description;

    public EventAppContext() {
    }

    public EventAppContext(final String id, final String title, final String description) {
        this.id = id;
        this.title = title;
        this.description = description;
    }

    public String getId() {
        return id;
    }

    public void setId(final String id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(final String title) {
        this.title = title;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(final String description) {
        this.description = description;
    }

    @Override public String toString() {
        return "EventAppContext{" +
                "id='" + id + '\'' +
                ", title='" + title + '\'' +
                ", description='" + description + '\'' +
                '}';
    }
}
